@OptIn(ExperimentalTextApi::class)
fun createFontFamily(
    @FontRes font: Int,
    vararg variation: FontVariation.Setting
): FontFamily {
    return FontFamily(
        listOf(
            FontWeight.Normal,
            FontWeight.Medium,
            FontWeight.SemiBold,
            FontWeight.Bold,
            FontWeight.Black
        ).map { weight ->
            Font(
                resId = font,
                weight = weight,
                variationSettings = FontVariation.Settings(
                    *buildList {
                        add(FontVariation.weight(weight.weight))
                        addAll(variation)
                    }.toTypedArray()
                )
            )
        }
    )
}

